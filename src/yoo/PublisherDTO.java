package yoo;

public class PublisherDTO {
	
	private String pub_code;
	private String pub_name;
	private String pub_manager;
	private String pub_email;
	private String pub_phone;
	
	
	public String getPub_code() {
		return pub_code;
	}
	public void setPub_code(String pub_code) {
		this.pub_code = pub_code;
	}
	public String getPub_name() {
		return pub_name;
	}
	public void setPub_name(String pub_name) {
		this.pub_name = pub_name;
	}
	public String getPub_manager() {
		return pub_manager;
	}
	public void setPub_manager(String pub_manager) {
		this.pub_manager = pub_manager;
	}
	public String getPub_email() {
		return pub_email;
	}
	public void setPub_email(String pub_email) {
		this.pub_email = pub_email;
	}
	public String getPub_phone() {
		return pub_phone;
	}
	public void setPub_phone(String pub_phone) {
		this.pub_phone = pub_phone;
	}
	
	
}
