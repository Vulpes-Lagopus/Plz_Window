package yoo;

public class PackageDTO {
	
	private String pack_id;
	private String pub_code;
	private String pack_name;
	private int pack_price;
	private String pack_date;
	
	
	public String getPack_id() {
		return pack_id;
	}
	public void setPack_id(String pack_id) {
		this.pack_id = pack_id;
	}
	public String getPub_code() {
		return pub_code;
	}
	public void setPub_code(String pub_code) {
		this.pub_code = pub_code;
	}
	public String getPack_name() {
		return pack_name;
	}
	public void setPack_name(String pack_name) {
		this.pack_name = pack_name;
	}
	public int getPack_price() {
		return pack_price;
	}
	public void setPack_price(int pack_price) {
		this.pack_price = pack_price;
	}
	public String getPack_date() {
		return pack_date;
	}
	public void setPack_date(String pack_date) {
		this.pack_date = pack_date;
	}
	
	
}
