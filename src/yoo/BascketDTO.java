package yoo;
	
public class BascketDTO {
	
	private String basc_number;
	private String mem_id;
	private String pack_id;
	private String event_code;
	private int basc_quantity;
	private String basc_date;
	
	
	public String getBasc_number() {
		return basc_number;
	}
	public void setBasc_number(String basc_number) {
		this.basc_number = basc_number;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getPack_id() {
		return pack_id;
	}
	public void setPack_id(String pack_id) {
		this.pack_id = pack_id;
	}
	public String getEvent_code() {
		return event_code;
	}
	public void setEvent_code(String event_code) {
		this.event_code = event_code;
	}
	public int getBasc_quantity() {
		return basc_quantity;
	}
	public void setBasc_quantity(int basc_quantity) {
		this.basc_quantity = basc_quantity;
	}
	public String getBasc_date() {
		return basc_date;
	}
	public void setBasc_date(String basc_date) {
		this.basc_date = basc_date;
	}
	
	
}
